                var data = "newsletter_email="+newsletter_email;
                $.ajax({
                    url: 'newsletterEmail.php',
                    type: 'POST',
                    data: data,
                    success: function(response) {
                        if(response == "success") {
                            $("#newslettermsg")("<span style='color: #00FF00;'>Thank you! You have successfully subscribed to the newsletter...</span>");
                        } else if(response == "error") {
                            $("#newslettermsg")("<span style='color: #FF0000;'>Newsletter subscription error! Please try again...</span>");
                        } else if(response == "already") {
                            $("#newslettermsg")("<span style='color: #FF0000;'>Email Address already exists...</span>");
                        } else if(response == "blank") {
                            $("#newslettermsg")("<span style='color: #FF0000;'>Email Address cannot be blank! Enter Email Address...</span>");
                        }
                    }
                });
            }
